// Taking Input From User

var user = prompt('Insert Value of Number of Rows');

for (var i = 1 ; i <= user ; i++) {
    for (var j = 1 ; j <= i ; j++) {
        document.write("*")
    }
    document.write('<br />');
}


    


var userValue = prompt('Insert Value of Number of second Row');
    
for (var i = 1 ; i <= userValue ; i++) {
    for (var j = 1 ; j <= i ; j++) {
        document.write(i);
        
    }
    document.write('<br />');
}

    
    